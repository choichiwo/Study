package Class4;

public class Read {
	/*객체지향언어의 특징
	 * 1.캡슐화
	 * 클래스(객체구현)
	 * 접근제한자(private,public,default,protected)
	 * 
	 * 2.상속
	 *   부모클래스(상위클래스,superclass)
	 *   자식클래스(하위클래스,subclass)
	 *   메소드 모버라이딩(Method Overriding)
	 *   (상위클래스의 메소드를 무시)
	 *   :하위클래스의 동명 메소드가 우선실행.
	 * 3.다형성
	 *   메소드 오버로딩(Method Overloading)
	 *    생성자 오버로딩(Constructor Overloading)
	 * 4.추상화
	 *   추상클래스(Abstract class)
	 *     메소드 오버라이딩을 이용.
	 *     불필요한 부모클래스의 메소드 선언을 회피.
	 *   인터페이스(Interface)
	 *     메소드 페이스만 제공      
	 *  Class A {
	 *  	필드(변수)
	 *  	생성자(new연산자 실행시 자동실행)
	 *  	메소드(내외부에서 명시적 호출)
	 *  }
	 *  
	 * 	abstract class A {
	 *  	필드(변수)
	 *  	생성자
	 *  	메소드
	 *  	추상메소드(body가 없음)
	 *  }
	 *  
	 *  interface I {
	 *  	상수
	 *  	[필드없음]
	 *  	[생성자없음]
	 *  	추상메소드(내외부에서 명시적 호출,body가 없음)
	 *  	디폴트메소드(body있음 from java8)
	 *  	정적메소드(body있음 from java8)
	 *  }  
	 *  
	 *  Class A{부모(상위)클래스}(Parent(SuperClass)) ->  Class A1 extends A{자식(하위)클래스}(Child(SubClass))
	 *  abstract class A{추상클래스} -> class A1 extends A{실체클래스}
	 *  interface I {인터페이스} -> class A1 implements I {구현클래스}
	 *      
	 */
}
